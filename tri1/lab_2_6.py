# user input
print("This program will calculate MPG for a car.")
miles = float(input("How many miles did you drive? "))
gallons = float(input("How much gas did you use? "))

# calculate MPG
mpg = miles / gallons

# output
print("The miles per gallon of your car is", mpg, "MPG")
