# user input
print("This program will convert degrees celsius to degrees fahrenheit.")
celsius = float(input("What is the temperature in celsius? "))

# calculations
fahrenheit = (celsius * (9 / 5)) + 32

# output
print(celsius, "°C is equal to ", fahrenheit, "°F", sep="")
